package com.leech;

public class SwitchWithCharChallenge {

    public static void charChecker(char charValue) {

        switch (charValue) {

            case 'A':
                System.out.println("A - was typed");
                break;

            case 'B':
                System.out.println("B - was typed");
                break;


            case 'C':
                System.out.println("C - was typed");
                break;

            case 'D':
                System.out.println("D - was typed");
                break;

            case 'E':
                System.out.println("E - was typed");
                break;


            default:
                System.out.println("None of the chars were typed");
                break;
        }

    }
}
